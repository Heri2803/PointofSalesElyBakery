    public void tabelproduk() {
        DefaultTableModel tb = new DefaultTableModel();
        //judul kolom
        tb.addColumn("Kode Produk");
        tb.addColumn("Nama Produk");
        tb.addColumn("Quantitas");
        tb.addColumn("Kategori");
        tb.addColumn("Harga Satuan");
        tabelproduk.setModel(tb);
        try{
            //mengambil data dari database
            res=stm.executeQuery("Select * from produk");
            while (res.next()){
                tb.addRow(new Object[]{
                    res.getString("KodeProduk"),
                    res.getString("NamaProduk"),
                    res.getString("Quantitas"),
                    res.getString("Kategori"),
                    res.getString("HargaSatuan")
                });
            }
              
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "data gagal ditampilkan" + e.getMessage());
        }    }
