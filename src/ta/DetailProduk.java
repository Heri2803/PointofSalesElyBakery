    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = tabelproduk.getSelectedRow();
    if (selectedRow >= 0) {
        Object[] rowData = new Object[tabelproduk.getColumnCount()]; // Array to store the row data

        // Fill the rowData array with data from the selected row
        for (int i = 0; i < tabelproduk.getColumnCount(); i++) {
            rowData[i] = tabelproduk.getValueAt(selectedRow, i);
        }

        // Convert rowData to String[]
        String[] rowDataAsString = new String[rowData.length];
        for (int i = 0; i < rowData.length; i++) {
            rowDataAsString[i] = String.valueOf(rowData[i]);
        }

        // Create a PopUpPelanggan object and pass the selected row data through the constructor
        PopUpProduk p = new PopUpProduk(DetailProduk.this, rowDataAsString);
        p.setVisible(true);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

