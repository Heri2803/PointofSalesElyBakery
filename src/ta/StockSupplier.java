    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = tabelsupplier.getSelectedRow();
    if (selectedRow >= 0) {
        Object[] rowData = new Object[tabelsupplier.getColumnCount()]; // Array to store the row data

        // Fill the rowData array with data from the selected row
        for (int i = 0; i < tabelsupplier.getColumnCount(); i++) {
            rowData[i] = tabelsupplier.getValueAt(selectedRow, i);
        }

        // Convert rowData to String[]
        String[] rowDataAsString = new String[rowData.length];
        for (int i = 0; i < rowData.length; i++) {
            rowDataAsString[i] = String.valueOf(rowData[i]);
        }

        // Create a PopUpPelanggan object and pass the selected row data through the constructor
        PopUpSupplier p = new PopUpSupplier(StockSupplier.this, rowDataAsString);
        p.setVisible(true);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

