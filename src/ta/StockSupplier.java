    public void tabelsupplier(){
        DefaultTableModel tb = new DefaultTableModel();
        //judul kolom
        tb.addColumn("Kode Supplier");
        tb.addColumn("Nama Supplier");
        tb.addColumn("Detail Bahan");
        tb.addColumn("Alamat");
        tb.addColumn("Telphone");
        tabelsupplier.setModel(tb);
        try{
            //m4ngwmbil data dari database
            res=stm.executeQuery("Select * from supplier");
            while (res.next()){
                tb.addRow(new Object[]{
                    res.getString("KodeSupplier"),
                    res.getString("NamaSupplier"),
                    res.getString("DetailBahan"),
                    res.getString("Alamat"),
                    res.getString("Telp"),
                });
            }
              
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "data gagal ditampilkan" + e.getMessage());
        }
    }
    
      public void carisupplier(String strg){
        model = (DefaultTableModel) tabelsupplier.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tabelsupplier.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(strg));
    }

